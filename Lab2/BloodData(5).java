class BloodData {
    private String bloodType;
    private String rhFactor;

    void setBloodType(String bloodType){
        this.bloodType = bloodType;
    }
    String getBloodType(){
        return bloodType;
    }
    void setRhFactor(String rhFactor){
        this.rhFactor = rhFactor;
    }
    String getRhFactor(){
        return rhFactor;
    }
}